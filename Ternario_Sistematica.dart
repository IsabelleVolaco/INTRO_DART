void main() {

// EXEMPLO CLASSE

  String reino = 'Animalia'; // Reino do organismo
  String classe;

  // Usando ternário dentro de ternário para determinar a classe
  classe = (reino == 'Plantae')
      ? 'Angiosperma'
      : (reino == 'Animalia')
          ? 'Mamífero'
          : (reino == 'Fungi')
              ? 'Basidiomycota'
              : 'Outro';

  print('Classe: $classe');  // Saída: Classe: Mamífero


// EXEMPLO FAMÍLIA

  String ordem = 'Carnivora'; // Ordem do organismo
  String familia;

  // Usando ternário dentro de ternário para determinar a família
  familia = (ordem == 'Carnivora')
      ? 'Felidae' // Gato e leão
      : (ordem == 'Primates')
          ? 'Hominidae' // Humanos e chimpanzés
          : (ordem == 'Rodentia')
              ? 'Muridae' // Ratos e camundongos
              : 'Outro';

  print('Família: $familia');  // Saída: Família: Felidae
}


